async function switchLanguage(language) {
    if (language !== 'ja' && language !== 'en') {
        language = 'ja';
    }
    
    currentLanguage = language;
    await saveLanguageSetting(language);
    
    // ãƒšãƒ¼ã‚¸ã‚’ãƒªãƒ­ãƒ¼ãƒ‰
    window.location.reload();
}

